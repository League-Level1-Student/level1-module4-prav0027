
public class Calculator {
public static void main(String[] args) {
	
}
private static int add(int value1, int value2) {
	int sum = value1+value2;
	return sum;
}
private static int subtract(int value1, int value2) {
	int difference = value1-value2;
	return difference;
}
private static int multiply(int value1, int value2) {
	int multiple = value1*value2;
	return multiple;
}
private static int division(int value1, int value2) {
	int divide = value1/value2;
	return divide;
}
}
